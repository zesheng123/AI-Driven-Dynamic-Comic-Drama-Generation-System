def select_prompt_profile(story_text: str = "", genre: str = "", characters=None):
    """根据当前故事选择可选 profile。

    注意：profile 只提供当前测试题材的镜头建议，不改变通用字段结构。
    """
    blob = " ".join([str(story_text or ""), str(genre or "")])
    for c in characters or []:
        if isinstance(c, dict):
            blob += " " + str(c.get("name", "")) + " " + str(c.get("description", ""))

    has_dragon = any(k in blob for k in ["远古巨龙", "巨龙", "恶龙", "古龙", "龙爪", "龙"])
    has_hero = any(k in blob for k in ["少年勇者", "勇者", "圣剑", "骑士", "剑士", "少年"])

    if has_dragon and has_hero:
        return HERO_DRAGON_PROFILE

    return None
